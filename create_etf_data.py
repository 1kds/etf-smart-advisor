import fitz  # PyMuPDF
import os

ETF_FAQ_CONTENT = """
ETF 투자 가이드 - 자주 묻는 질문 (FAQ)
========================================

[Q1] ETF란 무엇인가요?

ETF(Exchange Traded Fund, 상장지수펀드)는 특정 지수(인덱스)의 성과를 추적하도록 설계된 투자 상품으로, 주식시장에 상장되어 일반 주식처럼 실시간으로 매매할 수 있는 펀드입니다.

ETF는 주식과 펀드의 장점을 결합한 투자 상품입니다:
- 주식처럼 증권거래소에서 실시간 매매 가능
- 펀드처럼 여러 종목에 분산 투자 효과
- 일반 펀드보다 낮은 운용 보수(수수료)
- 높은 투명성 (보유 종목 매일 공개)

대표적인 예시로 KODEX 200은 코스피 200 지수를 추적하며, 해당 지수에 포함된 200개 종목에 분산 투자하는 것과 같은 효과를 제공합니다.


[Q2] ETF의 수수료 구조는 어떻게 되나요?

ETF 투자 시 발생하는 비용은 크게 세 가지입니다:

1. 총보수(TER, Total Expense Ratio): 연간 운용 보수로, ETF 가격에 이미 반영되어 있어 별도로 납부하지 않습니다. 보통 연 0.05%~0.50% 수준이며, 인덱스 ETF는 0.05%~0.15%로 매우 저렴합니다.

2. 매매 수수료: 주식 거래와 동일하게 증권사에 지불하는 위탁매매 수수료입니다. 온라인 거래 기준 약 0.015%~0.03% 수준입니다.

3. 세금: ETF 매매 차익에 대해 국내 주식형 ETF는 비과세이고, 해외 주식형·채권형·원자재 ETF는 배당소득세 15.4%가 과세됩니다.


[Q3] ETF는 어떻게 매매하나요?

ETF는 일반 주식과 동일한 방법으로 매매합니다:

1. 증권 계좌 개설: 증권사 앱(MTS) 또는 HTS에서 계좌를 개설합니다.
2. ETF 검색: 종목명이나 종목코드로 원하는 ETF를 검색합니다.
3. 주문 실행: 매수/매도 주문을 실행합니다. 지정가 또는 시장가 주문이 가능합니다.
4. 체결 확인: 주문이 체결되면 즉시 잔고에 반영됩니다.

거래 가능 시간은 한국거래소 정규 거래시간인 오전 9:00~오후 3:30입니다. 장 시작 전(8:00~9:00) 동시호가와 시간외 거래(15:40~16:00)도 가능합니다.


[Q4] 코스피 200 ETF와 S&P 500 ETF의 차이점은 무엇인가요?

두 ETF 모두 시장 대표 지수를 추적하지만, 투자 대상 시장이 다릅니다:

코스피 200 ETF:
- 한국 주식시장의 대표 200개 종목으로 구성
- 대표 상품: KODEX 200, TIGER 200
- 원화 기반 투자 (환율 위험 없음)
- 배당소득세 비과세 (국내 주식형)
- 삼성전자, SK하이닉스, 현대차 등 포함

S&P 500 ETF:
- 미국 주식시장의 대표 500개 종목으로 구성
- 대표 상품: TIGER 미국S&P500, KODEX 미국S&P500
- 달러 기반 투자 (환율 변동 영향 있음. 환헤지 상품도 있음)
- 배당소득세 15.4% 과세 (해외 주식형)
- 애플, 마이크로소프트, 아마존 등 포함


[Q5] 레버리지 ETF와 인버스 ETF란 무엇인가요?

레버리지 ETF:
- 기초 지수 수익률의 2배(또는 그 이상)를 추적하는 ETF
- 예: KODEX 레버리지는 코스피 200 일간 수익률의 2배를 추적
- 상승장에서 2배의 수익을 기대할 수 있으나, 하락장에서도 2배의 손실 발생
- 장기 보유 시 복리 효과로 인한 수익률 괴리(음의 복리 효과) 발생 가능
- 단기 트레이딩에 적합하며, 장기 투자에는 부적합

인버스 ETF:
- 기초 지수와 반대 방향으로 움직이는 ETF
- 예: KODEX 인버스는 코스피 200이 1% 하락하면 약 1% 상승
- 시장 하락을 예상할 때 또는 포트폴리오 헤지(위험 분산) 용도로 활용
- 레버리지 인버스(곱버스)는 하락의 2배를 추적

주의: 레버리지·인버스 ETF는 일간 수익률을 추적하므로, 장기 보유 시 기초 지수 대비 수익률이 크게 괴리될 수 있습니다. 투자 전 반드시 상품 특성을 이해해야 합니다.


[Q6] ETF 투자에서 추적오차란 무엇인가요?

추적오차(Tracking Error)는 ETF의 실제 수익률과 기초 지수 수익률 간의 차이를 의미합니다.

추적오차가 발생하는 원인:
- 운용 보수 차감
- 배당금 재투자 시점 차이
- 현금 보유 비중(Cash Drag)
- 종목 교체 시 거래 비용
- 샘플링 방식(지수 내 일부 종목만 보유)

추적오차가 작을수록 좋은 ETF입니다. 일반적으로 연간 추적오차가 0.5% 이내이면 우수한 수준으로 평가됩니다.

투자자는 ETF를 선택할 때 총보수뿐만 아니라 추적오차도 함께 비교해야 합니다.


[Q7] 분산투자와 자산배분 전략이란 무엇인가요?

분산투자는 "모든 달걀을 한 바구니에 담지 마라"는 투자 원칙입니다.

ETF를 활용한 분산투자 전략:

1. 자산군 분산: 주식 ETF + 채권 ETF + 원자재 ETF 등 서로 다른 자산에 나누어 투자
2. 지역 분산: 국내 ETF + 미국 ETF + 신흥국 ETF 등 여러 국가에 분산
3. 섹터 분산: IT, 헬스케어, 에너지 등 다양한 산업 섹터에 분산
4. 시간 분산: 정기적으로 일정 금액을 투자하는 적립식 투자(DCA)

대표적인 자산배분 모델:
- 보수적: 주식 30% + 채권 60% + 현금 10%
- 균형적: 주식 50% + 채권 40% + 대체자산 10%
- 공격적: 주식 70% + 채권 20% + 대체자산 10%


[Q8] ETF 투자 시 세금은 어떻게 되나요?

ETF 유형별 세금 구조:

1. 국내 주식형 ETF (KODEX 200 등):
   - 매매차익: 비과세
   - 분배금(배당): 배당소득세 15.4%
   - 가장 세금 혜택이 큰 유형

2. 해외 주식형 ETF (TIGER 미국S&P500 등):
   - 매매차익: 배당소득세 15.4% (과표기준가격 방식)
   - 분배금: 배당소득세 15.4%

3. 채권형, 원자재 ETF:
   - 매매차익: 배당소득세 15.4%
   - 분배금: 배당소득세 15.4%

절세 전략:
- ISA(개인종합자산관리계좌) 활용: 200만원(서민형 400만원)까지 비과세
- IRP/연금저축 활용: 세액공제 혜택 + 과세이연
- 국내 주식형 ETF 중심 투자: 매매차익 비과세


[Q9] NAV(순자산가치)란 무엇이며, 왜 중요한가요?

NAV(Net Asset Value, 순자산가치)는 ETF가 보유한 자산의 총 가치를 발행 좌수로 나눈 값입니다. 쉽게 말해, ETF 한 주의 실제 가치를 나타냅니다.

NAV = (ETF 보유 자산 총 가치 - 부채) ÷ 총 발행 좌수

ETF의 시장 가격이 NAV보다 높으면 '프리미엄' 상태, 낮으면 '디스카운트' 상태라고 합니다.

- 프리미엄 상태: 시장 가격 > NAV → 실제 가치보다 비싸게 거래 중
- 디스카운트 상태: 시장 가격 < NAV → 실제 가치보다 싸게 거래 중

투자자는 ETF를 매수할 때 NAV 대비 프리미엄/디스카운트 비율을 확인하고, 과도한 프리미엄 상태에서의 매수는 주의해야 합니다.


[Q10] ETF와 일반 펀드의 차이점은 무엇인가요?

ETF와 일반 펀드(공모펀드)의 주요 차이점:

| 항목 | ETF | 일반 펀드 |
|------|-----|-----------|
| 거래 방식 | 증권거래소에서 실시간 매매 | 운용사를 통해 하루 1회 기준가로 거래 |
| 가격 결정 | 실시간 시장 가격 | 하루 1회 기준가격(NAV) |
| 최소 투자금 | 1주 단위 (수천원~수만원) | 보통 10만원 이상 |
| 총보수 | 0.05%~0.50% (저렴) | 1.0%~2.5% (상대적으로 높음) |
| 투명성 | 보유 종목 매일 공시 | 분기별 공시 |
| 환매 수수료 | 없음 | 90일 미만 환매 시 수수료 |

ETF는 낮은 비용, 높은 유동성, 투명성 면에서 일반 펀드 대비 장점이 있습니다.
"""

def create_etf_faq_pdf(output_path: str):
    """ETF FAQ 내용을 PDF 파일로 생성합니다. (한글 폰트 사용)"""
    doc = fitz.open()
    
    # 페이지 설정
    page_width = 595  # A4
    page_height = 842
    margin = 50
    
    lines = ETF_FAQ_CONTENT.strip().split('\n')
    
    page = doc.new_page(width=page_width, height=page_height)
    y_position = margin
    
    for line in lines:
        # 구분선(===) 건너뛰기
        if line.startswith('==='):
            continue
        
        # 폰트 크기 결정
        if line.startswith('ETF 투자 가이드'):
            fontsize = 16
            y_position += 10
        elif line.startswith('[Q'):
            fontsize = 12
            y_position += 8
        else:
            fontsize = 10
        
        line_height = fontsize + 4
        
        # 페이지 넘김 체크
        if y_position > page_height - margin - 20:
            page = doc.new_page(width=page_width, height=page_height)
            y_position = margin
        
        # 한글 폰트(korea)로 텍스트 삽입
        page.insert_text(
            fitz.Point(margin, y_position),
            line,
            fontsize=fontsize,
            fontname="korea",
        )
        y_position += line_height
    
    doc.save(output_path)
    doc.close()
    print(f"ETF FAQ PDF 생성 완료: {output_path}")


if __name__ == "__main__":
    output = os.path.join(os.path.dirname(__file__), "data", "etf_faq.pdf")
    create_etf_faq_pdf(output)
